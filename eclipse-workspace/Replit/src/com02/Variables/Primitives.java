package com02.Variables;
/*
 * Declare an `byte` variable named `steps` and assign `110`

Declare an `short` variable named `miles` and assign `8700`

Declare an `int` variable named `count` and assign `1220000`

Declare an `long` variable named `population` and assign `3434455668`
 */

public class Primitives {

	public static void main(String[] args) {
		

	    byte steps = 110;
	    short miles = 8700;
	    int count = 1220000;
	    long population = 3434455668l;
	   
	    
	    //DO NOT TOUCH BELOW LINES.
	    System.out.println(steps);
	    System.out.println(miles);
	    System.out.println(count);
	    System.out.println(population);
	    

	}

}
